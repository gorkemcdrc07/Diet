let sonSahneId = null;

export function rastgeleDiyalogSahnesiGetir(
    kategori = "normal",
) {
    const liste =
        KARAKTER_DIYALOG_SAHNELERI[
        kategori
        ] ||
        KARAKTER_DIYALOG_SAHNELERI.normal;

    const kullanilabilir =
        liste.filter(
            (sahne) =>
                sahne.id !== sonSahneId,
        );

    const secilecekListe =
        kullanilabilir.length
            ? kullanilabilir
            : liste;

    const sahne =
        secilecekListe[
        Math.floor(
            Math.random() *
            secilecekListe.length,
        )
        ];

    sonSahneId = sahne.id;

    return sahne;
}